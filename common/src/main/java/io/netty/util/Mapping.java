package io.netty.util;

/**
 * 保持从一个类型的对象到另一个类型的对象的映射.
 */
public interface Mapping<IN, OUT> {

    /**
     * 根据传进去的 input，返回对应映射的 value
     */
    OUT map(IN input);
}
